namespace MatematikselişlemlerDll;
public class işlemler
{
    public int topla(int sayı1,int sayuı2)
    {
        return sayuı2 + sayı1;
    }
    public int çıkar(int sayı1, int sayuı2)
    {
        return sayuı2 - sayı1;
    }
    public int çarp(int sayı1, int sayuı2)
    {
        return sayuı2 * sayı1;
    }
    public int böl(int sayı1, int sayuı2)
    {
        return sayuı2 / sayı1;
    }

    
}

